package com.example.submissionbeginner;

public class Food {
    private String nameFood;
    private String detailFood;
    private String cityFood;
    private int photo;

    public String getCityFood() {
        return cityFood;
    }

    public void setCityFood(String cityFood) {
        this.cityFood = cityFood;
    }



    public String getNameFood() {
        return nameFood;
    }

    public void setNameFood(String nameFood) {
        this.nameFood = nameFood;
    }

    public String getDetailFood() {
        return detailFood;
    }

    public void setDetailFood(String detailFood) {
        this.detailFood = detailFood;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int  photo) {
        this.photo = photo;
    }
}
